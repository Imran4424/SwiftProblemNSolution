// 1.11. Write a program that declares and uses any Bool literals

/* 
        a boolean data type used to represent logical entities

        Boolean data types have only two possible values
                True
                False
*/

// Boolean value initialization
var examResult: Bool = false
print(examResult)

var mark: Int = 80

// result of the condition will be a boolean value, which is true
examResult = mark >= 40
print(examResult)

mark = 33
// result of the condition will be a boolean value, which is false
examResult = mark >= 40

print(examResult)


